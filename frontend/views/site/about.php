    <p>Функции</p>
    <ul>- упасть</ul>
    <ul>- съесть ($percent - процент откушенной части)</ul>
    <ul>- удалить (когда полностью съедено)</ul>

    <p> Переменные</p>
    <ul>- цвет (устанавливается при создании объекта случайным)</ul>
    <ul>- дата появления (устанавливается при создании объекта случайным unixTmeStamp)</ul>
    <ul>- дата падения (устанавливается при падении объекта с дерева)</ul>
    <ul>- статус (на дереве / упало)</ul>
    <ul>- сколько съели (%)</ul>
    <ul>- другие необходимые переменные, для определения состояния.</ul>

    <p>Состояния</p>
    <ul> - висит на дереве</ul>
    <ul> - упало/лежит на земле</ul>
    <ul> - гнилое яблоко</ul>

    <p>Условия</p>
    <ul>
        <li>Пока висит на дереве - испортиться не может.</li>
        <li>Когда висит на дереве - съесть не получится.</li>
        <li>После лежания 5 часов - портится.</li>
        <li>Когда испорчено - съесть не получится.</li>
        <li>Когда съедено - удаляется из массива яблок.</li>
    </ul>

    <p>Пример результирующего скрипта:</p>
    <ul>$apple = new Apple('green');</ul>

