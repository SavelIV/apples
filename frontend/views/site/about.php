    <ul>echo $apple->size; // 1 - decimal</ul>

    <ul>$apple->fallToGround(); // упасть на землю</ul>
    <ul>$apple->eat(25); // откусить четверть яблока</ul>
    <ul>echo $apple->size; // 0,75</ul>

